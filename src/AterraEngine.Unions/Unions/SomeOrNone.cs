public readonly partial struct SomeOrNone<T>() : IUnion<Some<T>, None>{
    public static implicit operator SomeOrNone<T>(T[]? value) {
        if (value is null) return new None();

        return value.Length switch {
            0 => new None(),
            _ => new Some<T>(value)
        };
    }
}
